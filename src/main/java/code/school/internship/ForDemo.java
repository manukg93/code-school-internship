package code.school.internship;

public class ForDemo {
    public static void main(String[] args) {

        for (int i = 0; i < 10; i++){
            System.out.println("The value of i is " + i);
        }

        int j = 2;
        while ( j < 4){
            System.out.println("the value of j is" + j);
            j++;
        }
    }
}

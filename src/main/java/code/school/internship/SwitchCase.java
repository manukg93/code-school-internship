package code.school.internship;

public class SwitchCase {
    public static void main(String[] args) {
        int digit = 5;

        // print given number using switch-case
        switch(digit)
        {
            case 1:
                System.out.println("Number: " + digit);
                break;
            case 2:
                System.out.println("Number: " + digit);
                break;
            case 3:
                System.out.println("Number: " + digit);
                break;
            case 4:
                System.out.println("Number: " + digit);
                break;
            case 5:
                System.out.println("Number: " + digit);
                break;
            case 6:
                System.out.println("Number: " + digit);
                break;
            case 7:
                System.out.println("Number: " + digit);
                break;
            case 8:
                System.out.println("Number: " + digit);
                break;
            case 9:
                System.out.println("Number: " + digit);
                break;
            default:
        }
    }
}
